// 1. Create two variables with names of two people
let name1 = "Alice";
let name2 = "Bob";

// 2. Use string concatenation to create a greeting
let greetingConcat = "Hello, " + name1 + " and " + name2 + "!";
console.log("Using concatenation:", greetingConcat);

// 3. Use a template string to create the same greeting
let greetingTemplate = `Hello, ${name1} and ${name2}!`;
console.log("Using template string:", greetingTemplate);