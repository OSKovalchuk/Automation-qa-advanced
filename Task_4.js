// Task 4.1: Area of a circle ---
let radius = 4; 
let circleArea = Math.PI * Math.pow(radius, 2); 
console.log("Area of the circle:", circleArea.toFixed(2));

// Task 4.2: Area of a rectangle ---
let length = 8; 
let width = 4;   
let rectangleArea = length * width;
console.log("Area of the rectangle:", rectangleArea.toFixed(2));

// Task 4.3: Volume of a cylinder 
let cylinderRadius = 2;  
let height = 5;          
let cylinderVolume = Math.PI * Math.pow(cylinderRadius, 2) * height; 
console.log("Volume of the cylinder:", cylinderVolume.toFixed(2));